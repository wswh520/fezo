package com.fz.enroll.entity.student;

public class Year {

	private int year;
	private int typeA;
	private int typeB;
	private int typeC;
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public int getTypeA() {
		return typeA;
	}
	public void setTypeA(int typeA) {
		this.typeA = typeA;
	}
	public int getTypeB() {
		return typeB;
	}
	public void setTypeB(int typeB) {
		this.typeB = typeB;
	}
	public int getTypeC() {
		return typeC;
	}
	public void setTypeC(int typeC) {
		this.typeC = typeC;
	}
}
